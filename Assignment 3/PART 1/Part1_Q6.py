#Q6:Write a function to take integer as argument and check if it is even or odd.
num=int(input("Enter an integer:"))
def function_mera (num):
    if num%2==0 :
        print(f"The {num} is an even number...")
    else :
        print(f"The {num} is an odd number...")
function_mera (num)
